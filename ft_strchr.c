#include "libft.h"

char *strchr(const char *s, int c)
{
	int len;
	int i;

	i = -1;
	len = (ft_strlen(s) + 1);
	while (i++ <= len)
	{
		if (s[i] == c)
		{
			return ((char *)&s[i]);
		}
	}
	return (NULL);
}
