#include "libft.h"

void	ft_putnbr(int nb)
{
	int neg;

	neg = 1;
	if (nb < 0)
	{
		neg = -1;
	}
	if (nb / 10 == 0)
	{
		if (neg == -1)
			ft_putchar('-');
		ft_putchar((nb * neg) + '0');
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putchar(((nb % 10) * neg) + '0');
	}
}
