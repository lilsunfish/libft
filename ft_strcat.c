char	*ft_strcat(char *s1, const char *s2)
{
	char *d;
	char *s;

	d = s1;
	s = (char *)s2;
	while (*d)
		d++;
	while (*s)
	{
		*d = *s;
		s++;
		d++;
	}
	*d = '\0';
	return (s1);
}
